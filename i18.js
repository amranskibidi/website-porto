let currentLang = localStorage.getItem('lang') || 'en';

function t(key) {
  return translations[currentLang][key] || translations['en'][key] || key;
}

function applyTranslations() {
  const lang = currentLang;
  document.documentElement.setAttribute('data-lang', lang);
  document.documentElement.setAttribute('lang', lang === 'id' ? 'id' : 'en');

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val) el.innerHTML = val;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = t(key);
    if (val) el.setAttribute('placeholder', val);
  });

  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.querySelector('.lang-en').classList.toggle('lang-active', lang === 'en');
    btn.querySelector('.lang-id').classList.toggle('lang-active', lang === 'id');
  });

  document.title = lang === 'id'
    ? 'AMRAN — Fullstack Developer'
    : 'AMRAN — Fullstack Developer';
}

function toggleLang() {
  currentLang = currentLang === 'en' ? 'id' : 'en';
  localStorage.setItem('lang', currentLang);

  document.body.style.opacity = '0.7';
  document.body.style.transition = 'opacity 0.2s ease';
  setTimeout(() => {
    applyTranslations();
    document.body.style.opacity = '1';
  }, 200);
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();

  document.getElementById('langToggle').addEventListener('click', toggleLang);

  const mobileLang = document.getElementById('langToggleMobile');
  if (mobileLang) mobileLang.addEventListener('click', toggleLang);
});

window.i18n = { t, currentLang: () => currentLang };
